## Das Kernprinzip: Überleben, Erkunden, Bauen
Man startet in einer zufälligen Welt und muss zunächst grundlegende Ressourcen sammeln (Holz, Stein), um Werkzeuge und eine erste Unterkunft zu bauen. Bei Nacht erscheinen gefährliche Monster wie **Creeper**, **Zombies** und **Skelette**, die das Überleben erschweren.

### Wichtigste Spielmodi im Überblick
| Spielmodus | Beschreibung | Ziel |
|------------|--------------|------|
| **Überleben** | Ressourcen sammeln, Gesundheit & Hunger managen, gegen Monster kämpfen. | Überleben & den Enderdrachen besiegen. |
| **Kreativ** | Unbegrenzte Ressourcen, Flugfähigkeit, keine Monster. | Frei bauen und gestalten ohne Einschränkungen. |
| **Abenteuer** | Speziell für benutzerdefinierte Maps – Blöcke können nur mit richtigen Werkzeugen abgebaut werden. | Story-basierte Herausforderungen meistern. |
| **Hardcore** | Wie Überleben, aber mit permanenterem Tod (Welt wird gelöscht). | So lange wie möglich überleben. |

## Was macht Minecraft so faszinierend?
*   **Kreativität ohne Grenzen:** Vom einfachen Haus bis zur Nachbildung des Planeten Saturn oder funktionierenden Taschenrechnern mit Redstone (dem ingenieurtechnischen Bauteil des Spiels) ist alles möglich.
*   **Ständige Weiterentwicklung:** Regelmäßige Updates (z.B. "Caves & Cliffs", "The Wild Update") fügen neue Biome, Blöcke, Kreaturen und Mechaniken hinzu.
    *   Beispiele für neue Biome: **Schluchten, Tropfsteinhöhlen, Mangrovensümpfe**.
*   **Vielseitige Community:** Die Spielerbasis reicht von jungen Kindern bis zu Erwachsenen und umfasst Baukünstler, Techniker, Abenteurer und Modder.
*   **Mods & Ressourcenpakete:** Die Spielerfahrung kann durch Community-Mods radikal verändert werden (z.B. *Tekkit*, *Pixelmon*). Offizielle **Marketplace**-Inhalte gibt es in den Bedrock-Editionen.

### Ein typischer "Erster Tag" in Minecraft (Überlebensmodus)
1.  **Holz schlagen** – für erste Werkzeuge.
2.  **Werkbank craften** – Herzstück des Crafting-Systems.
3.  **Steinwerkzeuge herstellen** – effizienter als Holz.
4.  **Schaf finden & Wolle sammeln** – für ein Bett.
5.  **Unterschlupf bauen** – vor der ersten Nacht.
6.  **Kohle oder Holz verkohlen** – für Fackeln.

## Plattformen & Editionen
Ursprünglich eine Java-PC-Version, gibt es Minecraft heute auf nahezu jeder Plattform:
*   **Java Edition** (PC): Die Originalversion, bekannt für umfangreiche Mods und Server-Customization.
*   **Bedrock Edition** (Windows 10/11, Konsolen, Mobile): Basiert auf C++, ermöglicht Crossplay zwischen Xbox, PlayStation, Switch, Windows und Mobilgeräten.
*   **Weitere:** **Minecraft Education Edition** (für Schulen), **Minecraft Legends** (Strategiespiel).

